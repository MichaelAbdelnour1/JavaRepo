public class JavaSmallConditional4 {
    public static void main(String[] args) {

        int x = 15;

        if (x < 10 || x > 20) {
            System.out.println("Out of Range");
        }

        if(x>= 10 && x <= 20){
            System.out.println("In Range");
        }


        int x1 = 5;

        if (x1 < 10 || x1 > 20) {
            System.out.println("Out of Range");
        }

        if(x1>= 10 && x1 <= 20){
            System.out.println("In Range");
        }
    }
}
